auto palette_fill_from_table_entry(
    u8 table, u8 entry, /* keys */
    struct PaletteEntry* palette
) -> bool {
    if (!palette) {
        return false;
    }

    if (table > PALETTE_TABLE_MAX || entry > PALETTE_ENTRY_MAX) {
        return false;
    }

    *palette = PALETTE_TABLES[table][entry];

    return true;
}

auto palette_fill_from_hash(
    u8 hash, /* key */
    u8 forth_byte, /* key */
    bool use_default, /* set to true to use the default palette if not found */
    struct PaletteEntry* palette
) -> bool {
    if (!palette) {
        return false;
    }

    for (auto& entry : PALETTE_HASH_ENTRIES) {
        if (hash == entry.hash) {
            /* some hashes collide, so the 4th byte of the title is used */
            /* luckily, all entries that collide has a non-zero 4th byte */
            if (entry.forth > 0 &&
                forth_byte != entry.forth) {
                continue;
            }

            return palette_fill_from_table_entry(
                entry.table,
                entry.entry,
                palette
            );
        }
    }

    // a default palette is used when no entries are found!
    if (use_default)
    {
        return palette_fill_from_hash(0x00, 0x00, false, palette);
    }

    return false;
}

auto palette_fill_from_buttons(
    u8 buttons, /* key */
    struct PaletteEntry* palette,
    struct PalettePreviewShades* preview /* optional (can be NULL) */
) -> bool {
    if (!palette) {
        return false;
    }

    for (const auto& entry : PALETTE_BUTTON_ENTRIES) {
        if (buttons == entry.buttons) {
            if (preview != nullptr) {
                *preview = entry.preview;
            }

            return palette_fill_from_table_entry(
                entry.table,
                entry.entry,
                palette
            );
        }
    }

    return false;
}

auto palette_fill_from_custom(
    enum CustomPalette custom, /* key */
    struct PaletteEntry* palette
) -> bool {
    if (custom >= CUSTOM_PALETTE_MAX) {
        return false;
    }

    if (!palette) {
        return false;
    }

    *palette = PALETTE_CUSTOM_TABLE[custom];

    return true;
}

